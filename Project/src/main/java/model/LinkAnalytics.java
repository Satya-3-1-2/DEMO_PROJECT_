package model;

public class LinkAnalytics {
    private int clicks;
    
    public LinkAnalytics() {
    }

    public LinkAnalytics(int clicks) {
        this.clicks = clicks;
    }

    public int getClicks() {
        return clicks;
    }

    public void setClicks(int clicks) {
        this.clicks = clicks;
    }
    
    
}
